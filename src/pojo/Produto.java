
package pojo;

public class Produto {
    private String nomeProduto;
    private int precoProduto;
    private int precoCustoProduto;
    private int codigoProduto;
    private int idFornecedorProduto;
    
   public String getNomeProduto (){
     return nomeProduto;
}
    public void setNomeProduto (){
        this.nomeProduto = nomeProduto;
    } 
    public int getPrecoProduto (){
     return precoProduto;
}
    public void setPrecoProduto (){
        this.precoProduto = precoProduto;
    }  
    public int getPrecoCustoProduto (){
     return precoCustoProduto;
}
    public void setPrecoCustoProduto (){
        this.precoCustoProduto = precoCustoProduto;
    } 
    public int getCodigoProduto (){
     return codigoProduto;
}
    public void setCodigoProduto (){
        this.codigoProduto = codigoProduto;
    } 
    public int getIdFornecedorProduto (){
     return idFornecedorProduto;
}
    public void setIdFornecedorProduto (){
        this.idFornecedorProduto = idFornecedorProduto;
    } 
}
